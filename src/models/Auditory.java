package com.sample;


public class Auditory {

  private long id;
  private long num;
  private String hall;


  public long getId() {
    return id;
  }

  public void setId(long id) {
    this.id = id;
  }


  public long getNum() {
    return num;
  }

  public void setNum(long num) {
    this.num = num;
  }


  public String getHall() {
    return hall;
  }

  public void setHall(String hall) {
    this.hall = hall;
  }

}
